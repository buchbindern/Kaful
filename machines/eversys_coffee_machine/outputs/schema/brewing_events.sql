CREATE SEQUENCE brewing_events_event_id_seq START 1;
CREATE TABLE brewing_events (
    event_id INTEGER PRIMARY KEY DEFAULT nextval('brewing_events_event_id_seq'),
    timestamp TIMESTAMP NOT NULL,
    machine_id VARCHAR(50) NOT NULL,
    product_button_pressed VARCHAR(50),
    dispensing_start_time TIMESTAMP,
    dispensing_stop_time TIMESTAMP,
    water_quantity INTEGER CHECK (water_quantity BETWEEN 0 AND 1000),
    milk_quantity DECIMAL(4,1) CHECK (milk_quantity BETWEEN 0 AND 100),
    cake_thickness DECIMAL(4,1),
    tamping_pressure INTEGER,
    pre_infusion_time DECIMAL(3,1),
    relax_time DECIMAL(3,1),
    second_tamping DECIMAL(3,1),
    extraction_time DECIMAL(4,1) CHECK (extraction_time BETWEEN 21 AND 23),
    water_temperature INTEGER,
    stop_temperature INTEGER,
    foam_texture INTEGER CHECK (foam_texture BETWEEN 50 AND 80),
    milk_delay_time DECIMAL(3,1),
    coffee_cycles INTEGER,
    bypass_quantity INTEGER CHECK (bypass_quantity >= 0 AND bypass_quantity <= 100),
    bean_hopper_selection VARCHAR(10) CHECK (bean_hopper_selection IN ('left', 'right')),
    manual_stop_pressed BOOLEAN,
    product_key_name VARCHAR(13),
    product_type VARCHAR(50),
    milk_temperature_setting VARCHAR(10),
    double_product BOOLEAN,
    manual_stop_triggered BOOLEAN,
    hot_water_duration DECIMAL(6,1) CHECK (hot_water_duration BETWEEN 0 AND 1000),
    powder_density DECIMAL(4,1),
    dispensing_end_time TIMESTAMP,
    error VARCHAR(50),
    success BOOLEAN NOT NULL
);